export type TranslationKey = keyof typeof TRANSLATIONS.FR;
export type LanguageType = "FR" | "EN";
export function getTranslation(lang: LanguageType, key: TranslationKey): string {
  return TRANSLATIONS[lang]?.[key] || TRANSLATIONS["FR"]?.[key] || String(key);
}
export function getFormattedDate(lang: LanguageType, dateString: string): string {
  try {
    const date = new Date(dateString);
    return date.toLocaleDateString(lang === "FR" ? "fr-TG" : "en-US", {
      weekday: "long",
      year: "numeric",
      month: "long",
      day: "numeric",
      hour: "2-digit",
      minute: "2-digit"
    });
  } catch (e) {
    return dateString;
  }
}
export function getShortFormattedDate(lang: LanguageType, dateString: string): string {
  try {
    const date = new Date(dateString);
    return date.toLocaleDateString(lang === "FR" ? "fr-TG" : "en-US", {
      year: "numeric",
      month: "short",
      day: "numeric"
    });
  } catch (e) {
    return dateString;
  }
}
